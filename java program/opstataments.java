class opstataments {
    public static void main(String[] args) {
        //backspace(b) "\b is use to remove last character before it"
        System.out.println();
        System.out.println("hello\bworld");//output:hellworld
        System.out.println("pathan\bsabiya");//output:pathasabiya
        //tab "the defalut operating system space take is 8"
        System.out.printf("A\tB\n");//output:A------B
        System.out.printf("sabiya\t!\n");//output:sabiya-!
        System.out.printf("c:\\new folder>\n");
        System.out.printf("Ram said \"I am going to guntur.\"\n");
        System.out.printf("sabiya said \"I am a full stack web developer.\"\n");
        System.out.printf("Ram said 'I am going to guntur.'\n");
        System.out.printf("\000\n");//null
        System.out.printf("\100\n");//@
        System.out.printf("\101\n");//A
        System.out.printf("\u0c09\n");
        System.out.println();
    }
    
}
